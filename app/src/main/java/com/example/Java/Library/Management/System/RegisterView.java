    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String username = usernameField.getText();
       String fullname = nameField.getText();
       String password = new String(passwordField.getPassword());
         String confirmPassword = new String(confirmPasswordField.getPassword());

         if (!password.equals(confirmPassword)) {
             javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
             return;
         }

         boolean isUserExists = new RegisterRepositoryImpl().isUserExists(username);

            if (isUserExists) {
                javax.swing.JOptionPane.showMessageDialog(this, "Username already exists!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            boolean isFullnameExists = new RegisterRepositoryImpl().isFullnameExists(fullname);
            if (isFullnameExists) {
                javax.swing.JOptionPane.showMessageDialog(this, "Full name already exists!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            boolean isRegistered = new RegisterRepositoryImpl().registerUser(username, password, fullname);
            if (isRegistered) {
                javax.swing.JOptionPane.showMessageDialog(this, "Registration successful! Please login.", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                LoginView loginView = new LoginView();
                loginView.setVisible(true);
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Registration failed! Please try again.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

