    void loadTables(){
        users.clear();
        tableModel.setRowCount(0);

        try {
            users = registerRepo.getUsers();
            for (UserModel user : users) {
                Object[] rowData = {
                        user.getId(),
                        user.getUsername(),
                        user.getFullname()
                };
                tableModel.addRow(rowData);
            }

        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, null, e);
        }

    }

